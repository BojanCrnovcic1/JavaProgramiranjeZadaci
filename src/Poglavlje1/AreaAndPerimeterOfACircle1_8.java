package Poglavlje1;

public class AreaAndPerimeterOfACircle1_8 {
    public static void main(String[] args) {

        double radius = 5.5;

        double obima = 2 * radius * Math.PI;
        double povrsina = radius * radius * Math.PI;

        System.out.println("Obim kruga je : "+obima);
        System.out.println("Povrsina kruga je: "+povrsina);
    }
}
